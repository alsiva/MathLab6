package compMath.Math;

public class Dot {

    private double x;
    private double y;

    public Dot(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double value) { this.x = value; }

    public double getY() {
        return y;
    }

    public void setY(double value) {
        this.y = value;
    }

}
